     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'value' => 'required',
        ]);

        $sharevalue = new ShareValue();
        $sharevalue->value = $request->input('value');
        $sharevalue->user_id = Auth()->user()->id;
        $sharevalue->save();

        return redirect('sharevalues')->with('success', 'Share value has been updated!');
    }

